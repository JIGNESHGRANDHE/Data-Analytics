# "=" Operator
x=4
x=3
print(x)    #OUTPUT=3
"""for now value of x is 3"""

# "+="  Operator
x+=6
print(x)   #OUTPUT=9
"""for now value of x is 9"""

#  "-="  Operator
x-=7
print(x)   #OUTPUT=2
"""for now value of x is 2"""

#  "*="   Operator
x*=2
print(x)  #OUTPUT=4
"""value of x is 4"""
